// PIVOT&QUEST ルーブリック評価表のコード化
// 出典: ルーブリック評価表【共通5軸】+ 各教材の固有軸、小論文評価観点シート、
//       面接評価観点シート、志望理由書要点ガイド(43要点)
//
// 採点方式（原典準拠）:
//   各軸をレベル1-4で判定 → 換算 4=1.0 / 3=0.75 / 2=0.5 / 1=0.25
//   総合点(100点満点) = 共通5軸の平均換算 × 60 + 固有軸の平均換算 × 40
//   ※テキスト演習で評価不能な軸(面接のノンバーバル等)は総合点の計算から除外し、
//     固有軸がすべて評価不能な場合は共通軸100%で算出する。

export type PracticeKind = "志望理由書" | "小論文" | "面接";

export interface RubricAxis {
  key: string;
  label: string;
  focus: string; // 観点の補足
  levels: Record<"4" | "3" | "2" | "1", string>;
  aiEvaluable: boolean; // テキスト解答から評価可能か
}

export interface Rubric {
  kind: PracticeKind;
  commonAxes: RubricAxis[];
  specificAxes: RubricAxis[];
  // 添削プロンプトに追加で渡す評価上の注意（減点基準・チェックリスト等）
  gradingNotes: string[];
  // 志望理由書のみ: カバレッジを判定する大項目
  checklistCategories?: string[];
}

